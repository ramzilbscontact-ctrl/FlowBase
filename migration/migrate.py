#!/usr/bin/env python3
"""
migrate.py — Radiance ERP MongoDB -> Supabase ETL orchestrator.

Reads all 19 MongoDB collections, builds a global ObjectId->UUID lookup table,
applies per-collection transform functions, and writes one SQL INSERT file per
Supabase table to migration/output/. Files are named with FK-order numeric
prefixes so they can be loaded in sequence with no FK violations.

Usage:
  cd /path/to/project/   (project root, not migration/)
  cp migration/.env.migration.example migration/.env.migration
  # Edit migration/.env.migration with real credentials
  python migration/migrate.py             # full run
  python migration/migrate.py --dry-run  # print counts only, no files written

Output files (in migration/output/):
  02_profiles.sql
  03_companies.sql
  04_contacts.sql
  05_pipeline_stages.sql
  06_deals.sql
  07_tasks.sql
  08_notes.sql
  09a_accounts_first_pass.sql     (all accounts with parent_id = NULL)
  09b_accounts_parent_update.sql  (UPDATE statements to restore parent_id)
  10_invoices.sql
  11_invoice_items.sql
  12_quotes.sql
  13_payments.sql
  14_journal_entries.sql
  15_journal_lines.sql
  16_departments.sql
  17_employees.sql
  18_leave_requests.sql
  19_payslips.sql
  20_google_tokens.sql
  21_audit_logs.sql

  migration/id_map.json  (ObjectId -> UUID mapping, useful for debugging)

Notes:
  - auth.users (file 01) is handled by load_users.py (plan 04-02), not here.
  - search_vector columns are never included (they are PostgreSQL generated columns).
  - accounts requires two passes: 09a inserts with parent_id=NULL, 09b updates.
  - payslips are deduplicated on (employee_id, period_month, period_year) to
    satisfy the Supabase UNIQUE constraint.
  - All SQL files use ON CONFLICT DO NOTHING for idempotent re-runs.
"""

